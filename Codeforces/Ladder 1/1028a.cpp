void solve() {
  ll n, m;
  cin >> n >> m;
  char arr[n][m];
  ll x = 0, y = 0, sum = 0;
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < m; j++) {
      cin >> arr[i][j];
      if (arr[i][j] == 'B') {
        sum += 1;
        x += i, y += j;
      }
    }
  }
  cout << x / sum + 1 << " " << y / sum + 1;
}

